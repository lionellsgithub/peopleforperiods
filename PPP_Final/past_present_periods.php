<h1>
A historic look at that unmentionable time of the month
</h1>
<img class="title-line" src="images/title-line.png"/>
<p>
Centuries of cycles and a progression of products reveal how society has addressed the business of menstruation throughout history.
</p>

<span class="t1span"></span>
<img class="line1" src="images/timeline/line1.png">
<h2>
Ancient Era
</h2>
<img class="timeline-illos" src="images/timeline/Timeline_R1_jf-01.png"/>
<p>
Ancient Egyption women fashion tampons out of 
softened papyrus. In other parts of the world, materials like soft wool, paper, vegetable fiber, grass, animal skin, and moss are used for makeshift tampons and pads. 
</p>

<img class="line1" src="images/timeline/line1.png">
<h2>
Middle Ages
</h2>
<p>
Peasant women mix menstrual blood with seed grain, using it as fertilizer.
</p>

<span class="t2span"></span>
<img class="line1" src="images/timeline/line1.png">
<div class="circle">1800s</div>
<p>
Women use rags under their dresses or bleed into their clothing. Suspenders and elastic belts eventually become popular for holding up menstrual cloths.
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">1867</div>
<p>
An early form of the menstrual cup attaches cup and wire to a belt.
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">1896</div>
<img class="timeline-illos" src="images/timeline/Timeline_R1_jf-03.png"/>
<p>
Johnson & Johnson introduces Lister’s Towels, the first commercial disposable sanitary napkin. But public modesty of the era makes them a commercial failure. 
</p>

<span class="t3span"></span>
<img class="line1" src="images/timeline/line1.png">
<div class="circle">1920</div>
<p>
Kimberly-Clarke introduces Kotex after French nurses in WWI used cellucotton bandages 
as sanitary napkins. 
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">1921</div>
<img class="timeline-illos" src="images/timeline/Timeline_R1_jf-04.png"/>
<p>
Kotex’s first advertising campaign appears in Ladies Home Journal, featuring words like dainty and illustrations of women in high society.
</p>

<span class="t4span"></span>
<img class="line1" src="images/timeline/line1.png">
<div class="circle">1928</div>
<p>
Johnson & Johnson’s Modess pad magazine advertising features “silent purchase coupons,” saving women the embarrassment of asking for pads aloud. 
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">1931</div>
<img class="timeline-illos" src="images/timeline/Timeline_R1_jf-05.png"/>
<p>
Dr. Earle Haas patents the first modern tampon, made of cotton with a cardboard tube applicator.
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">1948</div>
<img class="timeline-illos" src="images/timeline/Timeline_R1_jf-06.png"/>
<p>
The pad goes mainstream, as Modess launches its famous “Modess...because” campaign, enlisting notable designers, models, and photographers to create full-page ads of elegant, finely dressed women. 
</p>


<span class="t5span"></span>
<img class="line1" src="images/timeline/line1.png">
<div class="circle">1969</div>
<p>
Stayfree debuts the first pads with adhesive strips, eliminating the need for sanitary belts. 
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">1976</div>
<p>
Carefree introduces panty liners, opening up a new feminine hygiene market.
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">1980</div>
<img class="timeline-illos" src="images/timeline/Timeline_R1_jf-07.png"/>
<p>
Dozens of cases of Toxic Shock Syndrome result in a commercial recall and prompt further health studies and FDA approval processes. 
</p>

<span class="t6span"></span>
<img class="line1" src="images/timeline/line1.png">
<div class="circle">1985</div>
<img class="timeline-illos" src="images/timeline/1985.png"/>
<p>
A Tampax TV ad starring Courteney Cox features the first commercial use of the word “period.”
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">2006</div>
<img class="timeline-illos" src="images/timeline/2006.png"/>
<p>
Embracing new approaches to realistic advertising, Playtex Sport markets a new line of tampons for active women. 
</p>

<span class="t7span"></span>
<img class="line1" src="images/timeline/line1.png">
<div class="circle">2013</div>
<img class="timeline-illos" src="images/timeline/Timeline_R1_jf-02.png"/>
<p>
Raising $65,000 in a Kickstarter campaign for period-proof underwear, Miki and Radha Agrawal launch THINX absorbent undergarments. 
</p>

<span class="t8span"></span>
<img class="line1" src="images/timeline/line1.png">
<div class="circle">2014</div>
<p>
Sanitary protection records sales of $3 billion in the U.S., up 2% from the previous year.
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">2015</div>
<p>
The Always “Like A Girl” campaign runs a 60-second Super Bowl commercial, aimed at boosting girls’ confidence—reportedly costing up to $4.5 million.
</p>

<img class="line1" src="images/timeline/line1.png">
<div class="circle">2017</div>
<p>
Global Industry Analysts forecast the global feminine hygiene industry to hit $15.2 billion.
</p>

